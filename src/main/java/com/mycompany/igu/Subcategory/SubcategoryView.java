    private void btnDeleteSubcategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSubcategoryActionPerformed
        // TODO add your handling code here:
        int subcategoryId = getSelectedFromTable();
        if(subcategoryId > 0){
            try {
                controller.deleteSubcategory(subcategoryId);
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(SubcategoryView.class.getName()).log(Level.SEVERE, null, ex);
            }
            loadTable();
        }
    }//GEN-LAST:event_btnDeleteSubcategoryActionPerformed

