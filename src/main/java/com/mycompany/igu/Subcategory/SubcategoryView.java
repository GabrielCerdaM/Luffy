    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        String titulos[] = {"Id","Usuario","Rol"};
        model.setColumnIdentifiers(titulos);
        List<Subcategory> subcategories = controller.getSubcategories();
        if(subcategories != null){
            for(Subcategory sc: subcategories){
                Object[] object = new Object[]{sc.getId(),sc.getName(),sc.getDescription(),sc.getCategory().getName()};
                model.addRow(object);
            }
            tableSubcategory.setModel(model);
        }
    }
    
    private int getSelectedFromTable(){
        if(tableSubcategory.getRowCount() > 0){
            if(tableSubcategory.getSelectedRow() != -1){
                Object object = tableSubcategory.getValueAt(tableSubcategory.getSelectedRow(),0);
                int subcategoryId = Integer.parseInt(String.valueOf(object));
                return subcategoryId;
          }else{
            Utils.showMessage("Ninguna fila ha sido seleccionada", "Error", "Error");
          }
        }else{
              Utils.showMessage("La Lista se encuentra vacia", "Error", "Errors");          
        }
        return 0;
    }
    
