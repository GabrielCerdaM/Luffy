    private void loadTable(){
        DefaultTableModel model = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }            
        };
        
        String titles[] = {"Id","Name","Description"};
        model.setColumnIdentifiers(titles);
        List<Category> categories = controller.getCategories();
        if(categories != null){
            for(Category category: categories){
                Object[] object = new Object[]{category.getId(),category.getName(),category.getDescription()};
                model.addRow(object);
            }
            tableCategory.setModel(model);
        }
    }
    
    private int getSelectedFromTable(){
        if(tableCategory.getRowCount() > 0){
            if(tableCategory.getSelectedRow() != -1){
                Object object = tableCategory.getValueAt(tableCategory.getSelectedRow(),0);
                int userId = Integer.parseInt(String.valueOf(object));
                return userId;
          }else{
            Utils.showMessage("Ninguna fila ha sido seleccionada", "Error", "Error");
          }
        }else{
              Utils.showMessage("La Lista se encuentra vacia", "Error", "Errors");          
        }
        return 0;
    }
